.global _start
_start:
	MOV R0, #0xFF
	MOV R1, #11
	AND R2, R0, R1  // LOGICAL AND OPERATION (R2 = R0 AND R1)
	ANDS R3, R0, R1 // AND OPERATION WITH FLAGS SET
	ORR R4, R0, R1  // LOGICAL OR OPERATION
	EOR R5, R0, R1  // EXCLUSIVE OR 
	MVN R0,R0 // MOVE NEGATION 
	AND R0,R0, #0X000000FF // ANDING TO GET THE SPECIFIC VALUE ONLY
	
	MOV R7, #1
	SWI 0
	